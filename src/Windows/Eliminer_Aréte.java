    private void eliminer_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminer_btnActionPerformed
        int x;
        int y;
        int taille;
        if (n1.getText().length() < 1 || n2.getText().length() < 1) {
            JOptionPane.showMessageDialog(null, "Erreur.. Données manquantes : ");
        } else {
            x = Integer.parseInt(n1.getText());
            y = Integer.parseInt(n2.getText());

            if (x == y) {
                JOptionPane.showMessageDialog(null, "Erreur.. Vous devez taper un autre sommet : " + y);
            } else if (x < 0 || x >= i || y < 0 || y >= i) {
                JOptionPane.showMessageDialog(null, "Erreur.. Sommet non valide ");
            } else {
                graphe.setMatrice_adjacence(x, y, 0);
                graphe.setMatrice_adjacence(y, x, 0);
                graphe.setMatrice_Coefficients(x, y, 0);
                graphe.setMatrice_Coefficients(y, x, 0);
                n1.setText(null);
                n2.setText(null);
                Principal_Window.graphe_panel.paint(Principal_Window.graphe_panel.getGraphics());

                R_repaint();

            }
        }
    }//GEN-LAST:event_eliminer_btnActionPerformed

