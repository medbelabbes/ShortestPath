    public void R_repaint() {
        for (int j = 0; j < i; j++) {
            for (int k = 0; k < i; k++) {
                if (graphe.getMatrice_adjacence(j, k) == 1) {
                    Paint.Paint_Line(Principal_Window.graphe_panel.getGraphics(), graphe.getCordX(j), graphe.getCordY(j), graphe.getCordX(k), graphe.getCordY(k), graphe.getMatrice_Coefficients(j, k));
                }
            }
        }
        for (int j = 0; j < i; j++) {
            Paint.Paint_Cercle(Principal_Window.graphe_panel.getGraphics(), graphe.getCordX(j), graphe.getCordY(j), String.valueOf(graphe.getNombre(j)));

        }
    }

